        static readonly ElementSymbol[] all;
        /// <summary>
        /// All symbols. Does not include "Unknown".
        /// </summary>
        public static IEnumerable<ElementSymbol> All { get { return all; } }

        static ElementSymbols()
        {
            var et = typeof(ElementSymbol);
            List<ElementSymbol> allSymbols = new List<ElementSymbol>();
            foreach (var f in typeof(ElementSymbols).GetFields())
            {
                if (f.FieldType == et)
                {
                    var s = (ElementSymbol)f.GetValue(null);
                    if (s != Unknown && s != Empty) allSymbols.Add(s);
                }
            }

            all = allSymbols.ToArray();
        }
